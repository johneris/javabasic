public class Product1ToN {
    public static void main(String[] args)  {
        // 1 - 10
        int product = 1;
        for(int i = 1; i <= 10; i++)    product *= i;
        System.out.println("1 - 10 product: " + product);
        
        // 1 - 11
        product = 1;
        for(int i = 1; i <= 11; i++)    product *= i;
        System.out.println("1 - 11 product: " + product);
        
        // 1 - 12
        product = 1;
        for(int i = 1; i <= 12; i++)    product *= i;
        System.out.println("1 - 12 product: " + product);
        
        // 1 - 13
        product = 1;
        for(int i = 1; i <= 13; i++)    product *= i;
        System.out.println("1 - 13 product: " + product);
        
        // 1 - 14
        product = 1;
        for(int i = 1; i <= 14; i++)    product *= i;
        System.out.println("1 - 14 product: " + product);
    }
}
